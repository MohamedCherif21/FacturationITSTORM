<?php

namespace App\Repository;

use App\Entity\Facture;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Facture>
 *
 * @method Facture|null find($id, $lockMode = null, $lockVersion = null)
 * @method Facture|null findOneBy(array $criteria, array $orderBy = null)
 * @method Facture[]    findAll()
 * @method Facture[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class FactureRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Facture::class);
    }

    public function save(Facture $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Facture $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function findFactureByNum(string $query)
    {
        $qb = $this->createQueryBuilder('p');
        $qb
            ->where(
                $qb->expr()->andX(
                    $qb->expr()->orX(
                        $qb->expr()->like('p.id', ':query'),
                        $qb->expr()->like('p.numFacture', ':query'),
                  
                    ),
                    $qb->expr()->isNotNull('p.created_at')
                )
            )
            ->setParameter('query', '%' . $query . '%')
        ;
        return $qb
            ->getQuery()
            ->getResult();
    }
    public function getLastFactureNumber(): ?int    
    {
        return $this->createQueryBuilder('f')
            ->select('MAX(SUBSTRING(f.numFacture, 10))') // Récupérer uniquement le numéro de la dernière facture
            ->getQuery()
            ->getSingleScalarResult(); // Récupérer le résultat sous forme d'un seul scalaire
    }

    public function findNonPayeeFactures(): array
    {
        return $this->createQueryBuilder('f')
            ->where('f.etat = :etat')
            ->setParameter('etat', 'nonpayee')
            ->getQuery()
            ->getResult();
    }

